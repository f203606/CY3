//atribuições
let x = 10; //atribuição simples. x é 10
x += 5; //atribuição de adição. Agora x é 15.
x -= 3; //atribuição de subtração. Agora x é 12.
x *= 2; //atribuição de multiplicação. Agora x é 24.
x /= 4; //atribuição de divisão. Agora x é 6.
x %= 5; //atribuição de módulo. Agora x é 1.
console.log(x);

//operadores de comparação
console.log(5 == '5'); //igualdade (true)
console.log(5 === '5'); //igualdade estrita (false)
console.log(5 != '5'); //desigualdade (false)
console.log(5 !== '5'); //desigualdade estrita (true)
console.log(5 > 3); //maior que (true)
console.log(5 >= 5); //maior ou igual (true)
console.log(3 < 5); //menor que (true)
console.log(5 <= 5); //menor ou igual (true)

//operadores lógicos
console.log(true && false); //false
console.log(true || false); //true
console.log(!true); //false

//operadores ternários
let age = 18;
let canVote = (age >= 18) ? "Yes" : "No";
console.log(canVote);









